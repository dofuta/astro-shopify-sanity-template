// Admin API のメタフィールド定義を取得し、src/lib/metafields.generated.ts を生成する。
//
// 取得対象は ownerType: PRODUCT / PRODUCTVARIANT。
// Storefront から読める（access.storefront === "PUBLIC_READ"）スカラー型の定義だけを採用する
// （参照型 *_reference は初版では除外）。
//
// 必要な環境変数（.env から読み込む）:
//   - SHOPIFY_STORE_DOMAIN
//   - SHOPIFY_ADMIN_API_ACCESS_TOKEN（read_products スコープ）
//   - SHOPIFY_API_VERSION（任意・未指定時は 2026-01）

import { readFile, writeFile } from 'node:fs/promises';
import { existsSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const projectRoot = resolve(__dirname, '..');
const OUTPUT_PATH = resolve(projectRoot, 'src/lib/metafields.generated.ts');

// ─── .env の最小ローダー（dotenv 非依存） ─────────────────────────────────────
async function loadEnv() {
  const envPath = resolve(projectRoot, '.env');
  if (!existsSync(envPath)) return;
  const content = await readFile(envPath, 'utf8');
  for (const rawLine of content.split(/\r?\n/)) {
    const line = rawLine.trim();
    if (!line || line.startsWith('#')) continue;
    const eq = line.indexOf('=');
    if (eq === -1) continue;
    const key = line.slice(0, eq).trim();
    let value = line.slice(eq + 1).trim();
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }
    if (process.env[key] === undefined) process.env[key] = value;
  }
}

// ─── Admin API ────────────────────────────────────────────────────────────────
const DEFINITIONS_QUERY = /* GraphQL */ `
  query MetafieldDefs($ownerType: MetafieldOwnerType!, $after: String) {
    metafieldDefinitions(first: 250, ownerType: $ownerType, after: $after) {
      pageInfo {
        hasNextPage
        endCursor
      }
      edges {
        node {
          namespace
          key
          name
          type {
            name
          }
          access {
            storefront
          }
        }
      }
    }
  }
`;

async function adminFetch(url, token, variables) {
  const res = await fetch(url, {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
      'X-Shopify-Access-Token': token,
    },
    body: JSON.stringify({ query: DEFINITIONS_QUERY, variables }),
  });

  if (!res.ok) {
    throw new Error(`Admin API HTTP error: ${res.status} ${res.statusText}`);
  }

  const json = await res.json();
  if (json.errors?.length) {
    throw new Error(`Admin API GraphQL error: ${JSON.stringify(json.errors)}`);
  }
  return json.data;
}

async function fetchDefinitions(url, token, ownerType) {
  const defs = [];
  let after = null;
  let hasNextPage = true;

  while (hasNextPage) {
    const data = await adminFetch(url, token, { ownerType, after });
    const conn = data.metafieldDefinitions;
    for (const { node } of conn.edges) {
      defs.push(node);
    }
    hasNextPage = conn.pageInfo.hasNextPage;
    after = conn.pageInfo.endCursor;
  }

  return defs;
}

// Storefront 公開かつスカラー型（参照型は除外）だけを採用する
function isUsable(node) {
  const storefrontAccess = node.access?.storefront;
  if (storefrontAccess !== 'PUBLIC_READ') return false;
  const typeName = node.type?.name ?? '';
  if (typeName.includes('reference')) return false;
  return true;
}

function toDef(node) {
  return {
    namespace: node.namespace,
    key: node.key,
    label: node.name ?? node.key,
    type: node.type?.name ?? '',
  };
}

// ─── 生成 ───────────────────────────────────────────────────────────────────
function renderConstArray(name, defs) {
  if (defs.length === 0) {
    return `export const ${name} = [] as const satisfies readonly MetafieldDef[];`;
  }
  const lines = defs
    .map(
      (d) =>
        `  { namespace: ${JSON.stringify(d.namespace)}, key: ${JSON.stringify(d.key)}, label: ${JSON.stringify(
          d.label,
        )}, type: ${JSON.stringify(d.type)} },`,
    )
    .join('\n');
  return `export const ${name} = [\n${lines}\n] as const satisfies readonly MetafieldDef[];`;
}

function renderFile(productDefs, variantDefs) {
  return `// AUTO-GENERATED by scripts/gen-metafields.mjs. DO NOT EDIT.
//
// \`npm run gen:metafields\` を実行すると、Admin API のメタフィールド定義のうち
// Storefront から読める（access.storefront === "PUBLIC_READ"）スカラー型のものが
// ここに書き出される。
import type { MetafieldDef } from './metafields';

${renderConstArray('PRODUCT_METAFIELDS', productDefs)}

${renderConstArray('VARIANT_METAFIELDS', variantDefs)}
`;
}

async function main() {
  await loadEnv();

  const domain = process.env.SHOPIFY_STORE_DOMAIN;
  const token = process.env.SHOPIFY_ADMIN_API_ACCESS_TOKEN;
  const version = process.env.SHOPIFY_API_VERSION ?? '2026-01';

  if (!domain) throw new Error('SHOPIFY_STORE_DOMAIN is not set');
  if (!token) {
    throw new Error(
      'SHOPIFY_ADMIN_API_ACCESS_TOKEN is not set（開発時のみ必要。read_products スコープのカスタムアプリのトークンを .env に設定してください）',
    );
  }

  const url = `https://${domain}/admin/api/${version}/graphql.json`;

  const [productNodes, variantNodes] = await Promise.all([
    fetchDefinitions(url, token, 'PRODUCT'),
    fetchDefinitions(url, token, 'PRODUCTVARIANT'),
  ]);

  const productDefs = productNodes.filter(isUsable).map(toDef);
  const variantDefs = variantNodes.filter(isUsable).map(toDef);

  await writeFile(OUTPUT_PATH, renderFile(productDefs, variantDefs), 'utf8');

  console.log(`[gen-metafields] wrote ${OUTPUT_PATH}`);
  console.log(`[gen-metafields] product: ${productDefs.length} / variant: ${variantDefs.length} 件採用`);
  const skipped =
    productNodes.length + variantNodes.length - productDefs.length - variantDefs.length;
  if (skipped > 0) {
    console.log(`[gen-metafields] ${skipped} 件は非公開 or 参照型のため除外しました`);
  }
}

main().catch((err) => {
  console.error('[gen-metafields] 失敗:', err.message);
  process.exit(1);
});
